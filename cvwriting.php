                                                <h4></h4>
                                                <p>
                                                <p>
                                                The work experience section of your CV is where you show your professional track record. It needs to be accurate, highlight your achievements and include relevant points that prove you’re the right person for the job.
                                                But this can be hard to perfect. After all, what does the hiring manager actually want in this section? Have no fear; we explain our 5 tips for writing the experience section of your CV below.
                                                <br><br>
                                                • Draw parallels<br>
                                                                        List your previous jobs in chronological order with the most recent employment first. If there are some parallels between your old company and the new one you are applying to, then it’s worth providing information about their similarities. This can show the hiring manager that you have the skills for the job and it is important if your current company isn’t well known.
                                                                        <br>As an example, this might include stating that your previous employer worked with many clients in the media sector, if you know this is a key customer base for the company you’re applying to.

                                                                        <br><br>
                                                                            • Be factual<br>
                                                                            All aspects of your CV, including your dates of employment, must be factually correct. If you have a gap in employment and are worried about how this may come across, you can always enter dates as month-to-month or even year-to-year, rather than specific dates.
                                                                            <br>For jobs that were many years ago, or if you’ve had a number of jobs in a relatively short period, you can group these jobs, for example, ‘2001-2003 worked in temporary roles within the retail sector’.

                                                                            <br><br>
                                                                            • Prove you have what they want<br>
                                                                            Research the job details and personal specifications for the type of job you want. Then, under each of the jobs in your experience section, use bullet points to illustrate that you have exactly the skills and expertise they’re after.
                                                                            <br>Remember to be specific. If they want sales experience, then specify how many customers you dealt with, the types of responsibilities you had and give an example to show off your skills. For example:
                                                                            <br>‘I have experience in a demanding sales role where I was required to meet monthly customer targets. I dealt with X customers on a daily basis making X amount of sales in a period of X months, exceeding my target by X%. This role taught me how to handle a busy and dynamic environment. I learnt to adapt to different customer needs to meet my monthly goals.’

                                                                            <br><br>
                                                                            • Voluntary work counts<br>
                                                                            Where you have limited work experience, or are looking to change career entirely, other experience outside of paid employment could show your capabilities. This might include unpaid work placements, community activities, such as working with a charity or being a student representative for your course. Employers will respond positively to reading about any extracurricular activities you’re involved in, especially if they demonstrate energy and community spirit, because these are exactly the kind of behaviours they want in their employees.

                                                                            <br><br>
                                                                            • Highlight your achievements<br>
                                                                            Many job hunters write their CVs like a job description. They faithfully list the duties they performed and their responsibilities. However, they fail to show the positive impact of their teamwork or contribution to the organization as a whole.

